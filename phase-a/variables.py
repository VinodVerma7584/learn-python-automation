# Example of using variables in Python

# Assigning values to variables
name = "John Doe"
age = 30
is_developer = True
salary = 75000.50

# Printing variable values
print("Name:", name)
print("Age:", age)
print("Is Developer:", is_developer)
print("Salary:", salary)

# Updating variable values
age += 1  # Increment age by 1
salary += 5000  # Increment salary by 5000

print("\nUpdated Age:", age)
print("Updated Salary:", salary)